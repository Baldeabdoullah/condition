let number = 10;
switch (number) {
  case 1:
    console.log("janvier");
    break;
  case 2:
    console.log("fevrier");
    break;

  case 3:
    console.log("mars");
    break;

  case 4:
    console.log("Avril");
    break;

  case 5:
    console.log("mai");
    break;

  case 6:
    console.log("juin");
    break;
  case 7:
    console.log("juillet");
    break;
  case 8:
    console.log("Aout");
    break;
  case 9:
    console.log("septembre");
    break;

  case 10:
    console.log("Octobre");
    break;

  case 11:
    console.log("novembre");
    break;

  case 12:
    console.log("Decembre");
    break;

  default:
    console.log("nombre invalide");
}
